#include "libft.h"

void	ft_putnbr_fd(int i, int fd)
{
	char c;

	if (i == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
		return ;
	}
	if (i < 0)
	{
		write(fd, "-", 1);
		i = -i;
	}
	if (i >= 10)
	{
		ft_putnbr_fd(i / 10, fd);
		c = i % 10 + 48;
		write(fd, &c, 1);
	}
	if (i < 10)
	{
		c = i + 48;
		write(fd, &c, 1);
	}
}
