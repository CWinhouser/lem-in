#include "libft.h"

char	*ft_strcat(char *str1, const char *str2)
{
	int i;
	int j;

	j = 0;
	i = ft_strlen(str1);
	while (str2[j] != '\0')
	{
		str1[i] = str2[j];
		j++;
		i++;
	}
	str1[i] = '\0';
	return (str1);
}
